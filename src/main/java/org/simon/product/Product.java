package org.simon.product;

/**
 * Generic interface for product.
 * 
 * @author Simon
 *
 */
public interface Product {
	/**
	 * 
	 * @return product id
	 */
	String getId();

	/**
	 * 
	 * @return product name
	 */
	String getName();

	/**
	 * 
	 * @return product image url
	 */
	String getImageUrl();

	/**
	 * 
	 * @return product price
	 */

}
